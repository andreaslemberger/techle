const validSet = new Set([
  ...TECH_WORD_LIST.map((w) => w.toLowerCase()),
  ...VALID_ENGLISH.map((w) => w.toLowerCase()),
]);

export function pickRandomWord(): string {
  const idx = Math.floor(Math.random() * TECH_WORD_LIST.length);
  return TECH_WORD_LIST[idx];
}

export function isValidGuess(word: string): boolean {
  return validSet.has(word.toLowerCase());
}

export type LetterState = "correct" | "present" | "absent" | "empty";

export function evaluateGuess(guess: string, solution: string): LetterState[] {
  const result: LetterState[] = new Array(solution.length).fill("absent");
  const solutionChars = solution.split("");
  const guessChars = guess.split("");
  const remaining: (string | null)[] = [...solutionChars];

  // First pass: mark correct (green)
  for (let i = 0; i < guessChars.length; i++) {
    if (guessChars[i] === solutionChars[i]) {
      result[i] = "correct";
      remaining[i] = null;
    }
  }

  // Second pass: mark present (yellow)
  for (let i = 0; i < guessChars.length; i++) {
    if (result[i] === "correct") continue;
    const idx = remaining.indexOf(guessChars[i]);
    if (idx !== -1) {
      result[i] = "present";
      remaining[idx] = null;
    }
  }

  return result;
}
